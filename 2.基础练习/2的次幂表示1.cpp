#include<stdio.h>
/*定义函数*/
void cimi(int n){
    int num=0;
    int i=0,j,k;
    int a[32];//数组定义为局部变量 
    while(n){
        j=n%2;
        if(j==1)
            a[num++]=i;//存储第几次是1
        i++;
        n/=2;
    }
    for(i=num-1;i>=0;i--){
        if(a[i]==0)
            printf("2(0)");
        else if(a[i]==1)
            printf("2");
        else if(a[i]==2)
            printf("2(2)");
        else if(a[i]>2){
            printf("2(");
            cimi(a[i]);//递归调用 
            printf(")");
        }
        if(i!=0)
            printf("+");
    }
} 
main(){
    int n;
    /*当输入的非结束指令时程序不终止*/
    while(scanf("%d",&n)!=EOF){
        cimi(n);//调用函数 
        printf("\n");
    }
}
