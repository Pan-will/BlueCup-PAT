#include<stdio.h>
#include<string.h>
int main(){
	double l,j;//l:利润;t:奖金总数。 
	scanf("%lf",&l);
	if(l<=100000){
		j=l*0.1;
	}else if(l>100000 && l<=200000){
		j=100000*0.1+(l-100000)*7.5/100;
		printf("%.2lf",j);
	}else if(l>200000 && l<=400000){
		j=100000*0.1+100000*7.5/100+(l-200000)*5/100;
		printf("%.2lf",j);
	}else if(l>400000 && l<=600000){
		j=100000*0.1+100000*7.5/100+200000*5/100+(l-400000)*3/100;
		printf("%.2lf",j);
	}else if(l>600000 && l<=1000000){
		j=100000*0.1+100000*7.5/100+200000*5/100+200000*3/100+(l-600000)*1.5/100;
		printf("%.2lf",j);
	}else if(l>1000000){
		j=100000*0.1+100000*7.5/100+200000*5/100+200000*3/100+400000*1.5/100+(l-1000000)*1/100;
		printf("%.2lf",j);
	}
	return 0;
}
