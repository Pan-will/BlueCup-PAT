/*
	将i~i(i=0-9)变化的步骤用数组保存。 
*/ 
#include<stdio.h>
int main(){
	int s[10][10]={
		{0, 4, 3, 3, 4, 3, 2, 3, 1, 2},  
        {4, 0, 5, 3, 2, 5, 6, 1, 5, 4},  
        {3, 5, 0, 2, 5, 4, 3, 4, 2, 3},  
        {3, 3, 2, 0, 3, 2, 3, 2, 2, 1},  
        {4, 2, 5, 3, 0, 3, 4, 3, 3, 2},  
        {3, 5, 4, 2, 3, 0, 1, 4, 2, 1},  
        {2, 6, 3, 3, 4, 1, 0, 5, 1, 2},  
        {3, 1, 4, 2, 3, 4, 5, 0, 4, 3},
        {1, 5, 2, 2, 3, 2, 1, 4, 0, 1},  
        {2, 4, 3, 1, 2, 1, 2, 3, 1, 0}
	};
	int n,ans;
	scanf("%d",&n);
	char a[n],b[n];
	scanf("%s\n%s",&a,&b);
	for(int i=0;i<n;i++){
		ans += s[a[i]-'0'][b[i]-'0'];
	}
	printf("%d",ans);
	return 0;
}
